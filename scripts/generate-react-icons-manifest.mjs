import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, '..')

const searchablePacks = [
  { key: 'si', label: 'Simple Icons', prefix: 'Si' },
  { key: 'fa6', label: 'Font Awesome 6', prefix: 'Fa' },
  { key: 'md', label: 'Material Design', prefix: 'Md' },
  { key: 'lu', label: 'Lucide', prefix: 'Lu' },
  { key: 'tb', label: 'Tabler', prefix: 'Tb' },
]

const splitIconNameWords = (value = '') => value.match(/[A-Z]+(?![a-z])|[A-Z]?[a-z]+|[0-9]+/g) ?? [value]

const normalizeKeyword = (value = '') =>
  value
    .toString()
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()

const buildLabel = (iconName, prefix) => {
  const withoutPrefix = iconName.startsWith(prefix) ? iconName.slice(prefix.length) : iconName
  return splitIconNameWords(withoutPrefix).join(' ').trim() || iconName
}

const buildKeywords = ({ iconName, label, pack, packLabel, prefix }) => {
  const withoutPrefix = iconName.startsWith(prefix) ? iconName.slice(prefix.length) : iconName
  const labelWords = splitIconNameWords(withoutPrefix)
  const rawKeywords = [
    iconName,
    withoutPrefix,
    label,
    labelWords.join(' '),
    labelWords.join(''),
    pack,
    packLabel,
  ]

  return [...new Set(rawKeywords.map(normalizeKeyword).filter(Boolean))]
}

const manifestEntries = searchablePacks.flatMap(({ key, label, prefix }) => {
  const definitionPath = path.join(repoRoot, 'node_modules', 'react-icons', key, 'index.d.ts')
  const definitionFile = readFileSync(definitionPath, 'utf8')
  const matches = [...definitionFile.matchAll(/^export declare const (\w+): IconType;$/gm)]

  return matches.map((match) => {
    const iconName = match[1]
    const iconLabel = buildLabel(iconName, prefix)

    return {
      id: `${key}:${iconName}`,
      pack: key,
      packLabel: label,
      iconName,
      label: iconLabel,
      keywords: buildKeywords({
        iconName,
        label: iconLabel,
        pack: key,
        packLabel: label,
        prefix,
      }),
    }
  })
})

const packOrder = searchablePacks.map((pack) => pack.key)

manifestEntries.sort((left, right) => {
  const leftPackIndex = packOrder.indexOf(left.pack)
  const rightPackIndex = packOrder.indexOf(right.pack)

  if (leftPackIndex !== rightPackIndex) {
    return leftPackIndex - rightPackIndex
  }

  const labelComparison = left.label.localeCompare(right.label)

  if (labelComparison !== 0) {
    return labelComparison
  }

  return left.iconName.localeCompare(right.iconName)
})

const outputDirectory = path.join(repoRoot, 'src', 'data')
const outputPath = path.join(outputDirectory, 'reactIconsManifest.js')

mkdirSync(outputDirectory, { recursive: true })

const fileContent = `// Generated by scripts/generate-react-icons-manifest.mjs
// Do not edit manually.

export const reactIconsManifest = ${JSON.stringify(manifestEntries, null, 2)}

export default reactIconsManifest
`

writeFileSync(outputPath, fileContent, 'utf8')

console.log(`Generated react-icons manifest with ${manifestEntries.length} icons at ${outputPath}`)
